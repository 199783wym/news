public class ProcessControl{
public static void main(String[] args) {
    int placing = 1;
    if (placing == 1) {
        System.out.println("1");
    } else {
        if (placing == 2) {
            System.out.println("2");
        } else {
            if (placing == 3) {
                System.out.println("3");
            } else {
                System.out.println("4");
            }
        }
    }
}
}